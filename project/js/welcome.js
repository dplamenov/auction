function toRegister() {
    let login = document.getElementById('login');
    let register = document.getElementById('register');
    login.style.display = 'none';
    register.style.display = 'block';
}
function toLogin() {
    let login = document.getElementById('login');
    let register = document.getElementById('register');
    login.style.display = 'block';
    register.style.display = 'none';
}
